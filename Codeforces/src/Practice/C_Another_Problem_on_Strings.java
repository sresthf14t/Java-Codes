import java.util.*;
import java.io.*;
public class C_Another_Problem_on_Strings {
    static class Scan {
        private byte[] buf=new byte[1024];
        private int index;
        private InputStream in;
        private int total;
        public Scan()
        {
            in=System.in;
        }
        public int scan()throws IOException
        {
            if(total<0)
            throw new InputMismatchException();
            if(index>=total)
            {
                index=0;
                total=in.read(buf);
                if(total<=0)
                return -1;
            }
            return buf[index++];
        }
        public int scanInt()throws IOException
        {
            int integer=0;
            int n=scan();
            while(isWhiteSpace(n))
            n=scan();
            int neg=1;
            if(n=='-')
            {
                neg=-1;
                n=scan();
            }
            while(!isWhiteSpace(n))
            {
                if(n>='0'&&n<='9')
                {
                    integer*=10;
                    integer+=n-'0';
                    n=scan();
                }
                else throw new InputMismatchException();
            }
            return neg*integer;
        }
        public double scanDouble()throws IOException
        {
            double doub=0;
            int n=scan();
            while(isWhiteSpace(n))
            n=scan();
            int neg=1;
            if(n=='-')
            {
                neg=-1;
                n=scan();
            }
            while(!isWhiteSpace(n)&&n!='.')
            {
                if(n>='0'&&n<='9')
                {
                    doub*=10;
                    doub+=n-'0';
                    n=scan();
                }
                else throw new InputMismatchException();
            }
            if(n=='.')
            {
                n=scan();
                double temp=1;
                while(!isWhiteSpace(n))
                {
                    if(n>='0'&&n<='9')
                    {
                        temp/=10;
                        doub+=(n-'0')*temp;
                        n=scan();
                    }
                    else throw new InputMismatchException();
                }
            }
            return doub*neg;
        }
        public String scanString()throws IOException
        {
            StringBuilder sb=new StringBuilder();
            int n=scan();
            while(isWhiteSpace(n))
            n=scan();
            while(!isWhiteSpace(n))
            {
                sb.append((char)n);
                n=scan();
            }
            return sb.toString();
        }
        private boolean isWhiteSpace(int n)
        {
            if(n==' '||n=='\n'||n=='\r'||n=='\t'||n==-1)
            return true;
            return false;
        }
    }
    public static void main(String args[]) throws IOException {
        Scan input=new Scan();
        int k=input.scanInt();
        String str=input.scanString();
        System.out.println(solve(str,k));
    }
    public static long solve(String str,int k) {
        int prefix[]=new int[str.length()];
        for(int i=0;i<str.length();i++) {
            if(i==0) {
                prefix[i]=str.charAt(i)-48;
                continue;
            }
            prefix[i]=prefix[i-1]+str.charAt(i)-48;
        }
        long ans=0;
        HashMap<Integer,Integer> map=new HashMap<>();
        for(int i=0;i<prefix.length;i++) {
            if(prefix[i]==k) {
                ans++;
            }
            if(map.containsKey(prefix[i]-k)) {
                ans+=map.get(prefix[i]-k);
            }
            if(!map.containsKey(prefix[i])) {
                map.put(prefix[i], 0);
            }
            map.replace(prefix[i], map.get(prefix[i])+1);
        }
        return ans;
    }
}
