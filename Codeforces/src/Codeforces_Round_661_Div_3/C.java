import java.util.*;
import java.io.*;
public class C {
    static class Scan {
        private byte[] buf=new byte[1024];
        private int index;
        private InputStream in;
        private int total;
        public Scan()
        {
            in=System.in;
        }
        public int scan()throws IOException
        {
            if(total<0)
            throw new InputMismatchException();
            if(index>=total)
            {
                index=0;
                total=in.read(buf);
                if(total<=0)
                return -1;
            }
            return buf[index++];
        }
        public int scanInt()throws IOException
        {
            int integer=0;
            int n=scan();
            while(isWhiteSpace(n))
            n=scan();
            int neg=1;
            if(n=='-')
            {
                neg=-1;
                n=scan();
            }
            while(!isWhiteSpace(n))
            {
                if(n>='0'&&n<='9')
                {
                    integer*=10;
                    integer+=n-'0';
                    n=scan();
                }
                else throw new InputMismatchException();
            }
            return neg*integer;
        }
        public double scanDouble()throws IOException
        {
            double doub=0;
            int n=scan();
            while(isWhiteSpace(n))
            n=scan();
            int neg=1;
            if(n=='-')
            {
                neg=-1;
                n=scan();
            }
            while(!isWhiteSpace(n)&&n!='.')
            {
                if(n>='0'&&n<='9')
                {
                    doub*=10;
                    doub+=n-'0';
                    n=scan();
                }
                else throw new InputMismatchException();
            }
            if(n=='.')
            {
                n=scan();
                double temp=1;
                while(!isWhiteSpace(n))
                {
                    if(n>='0'&&n<='9')
                    {
                        temp/=10;
                        doub+=(n-'0')*temp;
                        n=scan();
                    }
                    else throw new InputMismatchException();
                }
            }
            return doub*neg;
        }
        public String scanString()throws IOException
        {
            StringBuilder sb=new StringBuilder();
            int n=scan();
            while(isWhiteSpace(n))
            n=scan();
            while(!isWhiteSpace(n))
            {
                sb.append((char)n);
                n=scan();
            }
            return sb.toString();
        }
        private boolean isWhiteSpace(int n)
        {
            if(n==' '||n=='\n'||n=='\r'||n=='\t'||n==-1)
            return true;
            return false;
        }
    }
    public static void main(String args[]) throws IOException {
        Scan input=new Scan();
        StringBuilder ans=new StringBuilder("");
        int test=input.scanInt();
        for(int t=1;t<=test;t++) {
            int n=input.scanInt();
            int arr[]=new int[n];
            for(int i=0;i<n;i++) {
                arr[i]=input.scanInt();
            }
            ans.append(solve(n,arr)+"\n");
        }
        System.out.println(ans);
    }
    public static int solve(int n,int arr[]) {
        int max=0,max_wei=-1;
        for(int i=0;i<n;i++) {
            for(int j=i+1;j<n;j++) {
                int cnt=cnt(n,arr,arr[i]+arr[j]);
                if(cnt>max) {
                    max=cnt;
                    max_wei=arr[i]+arr[j];
                }
            }
        }
        return max;
    }
    public static int cnt(int n,int arr[],int weight) {
        int cnt=0;
        int freq[]=new int[100];
        for(int i=0;i<n;i++) {
            freq[arr[i]]++;
        }
        for(int i=0;i<freq.length;i++) {
            if(freq[i]==0) {
                continue;
            }
            int req=weight-i;
            if(req==i) {
                if(freq[i]>1) {
                   cnt++;
                    freq[i]-=2;
                    i--; 
                } 
            }
            else if(req>=0 && freq[req]>0) {
                freq[i]--;
                freq[req]--;
                cnt++;
                i--;
            }
        }
//        System.out.println(weight+" "+cnt);
        return cnt;
    }
}
