import java.util.*;
import java.io.*;
public class C_Vanya_and_Scales {
    static class Scan {
        private byte[] buf=new byte[1024];
        private int index;
        private InputStream in;
        private int total;
        public Scan()
        {
            in=System.in;
        }
        public int scan()throws IOException
        {
            if(total<0)
            throw new InputMismatchException();
            if(index>=total)
            {
                index=0;
                total=in.read(buf);
                if(total<=0)
                return -1;
            }
            return buf[index++];
        }
        public int scanInt()throws IOException
        {
            int integer=0;
            int n=scan();
            while(isWhiteSpace(n))
            n=scan();
            int neg=1;
            if(n=='-')
            {
                neg=-1;
                n=scan();
            }
            while(!isWhiteSpace(n))
            {
                if(n>='0'&&n<='9')
                {
                    integer*=10;
                    integer+=n-'0';
                    n=scan();
                }
                else throw new InputMismatchException();
            }
            return neg*integer;
        }
        public double scanDouble()throws IOException
        {
            double doub=0;
            int n=scan();
            while(isWhiteSpace(n))
            n=scan();
            int neg=1;
            if(n=='-')
            {
                neg=-1;
                n=scan();
            }
            while(!isWhiteSpace(n)&&n!='.')
            {
                if(n>='0'&&n<='9')
                {
                    doub*=10;
                    doub+=n-'0';
                    n=scan();
                }
                else throw new InputMismatchException();
            }
            if(n=='.')
            {
                n=scan();
                double temp=1;
                while(!isWhiteSpace(n))
                {
                    if(n>='0'&&n<='9')
                    {
                        temp/=10;
                        doub+=(n-'0')*temp;
                        n=scan();
                    }
                    else throw new InputMismatchException();
                }
            }
            return doub*neg;
        }
        public String scanString()throws IOException
        {
            StringBuilder sb=new StringBuilder();
            int n=scan();
            while(isWhiteSpace(n))
            n=scan();
            while(!isWhiteSpace(n))
            {
                sb.append((char)n);
                n=scan();
            }
            return sb.toString();
        }
        private boolean isWhiteSpace(int n)
        {
            if(n==' '||n=='\n'||n=='\r'||n=='\t'||n==-1)
            return true;
            return false;
        }
    }
    public static void main(String args[]) throws IOException {
        Scan input=new Scan();
        int w=input.scanInt();
        int m=input.scanInt();
        ArrayList<Integer> arr=new ArrayList<>();
        while(m!=0) {
            arr.add(m%w);
            m/=w;
        }
        arr.add(0);
        for(int i=0;i<arr.size();i++) {
            if(arr.get(i)==w) {
                if(i<arr.size()) {
                    arr.set(i+1, arr.get(i+1)+1);
                    arr.set(i, 0);
                }
                else {
                    arr.add(1);
                    arr.set(i, 0);
                }
            }
            if(arr.get(i)==0 || arr.get(i)==1) {
                continue;
            }
            else if(arr.get(i)==w-1) {
                if(i<arr.size()) {
                    arr.set(i+1, arr.get(i+1)+1);
                    arr.set(i, 0);
                }
                else {
                    arr.add(1);
                    arr.set(i, 0);
                }
            }
            else {
                System.out.println("No");
                return;
            }
        }
        System.out.println("Yes");
    }
}
