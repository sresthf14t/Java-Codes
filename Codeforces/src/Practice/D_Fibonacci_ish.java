    public static int solve(HashMap<Long,Integer> map,long a,long b) {
//        System.out.println(a+" "+b);
        int ans=2;
        map1=new HashMap<>();
        if(!map1.containsKey(a)) {
            map1.put(a, 1);
        }
        else {
            map1.replace(a, map1.get(a)+1);
        }
        
        if(!map1.containsKey(b)) {
            map1.put(b, 1);
        }
        else {
            map1.replace(b, map1.get(b)+1);
        }
        
        while(true) {
            if(!map.containsKey(a+b)) {
                break;
            }
            if(map1.containsKey(a+b)) {
                if(map1.get(a+b)+1>map.get(a+b)) {
                    break;
                }
            }
            if(!map1.containsKey(a+b)) {
                map1.put(a+b, 1);
            }
            else {
                map1.replace(a+b, map1.get(a+b)+1);
            }
            long tmp=a+b;
            a=b;
            b=tmp;
            ans++;
        }
        return ans;
    }
