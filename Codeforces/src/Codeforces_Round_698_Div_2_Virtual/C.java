    public static boolean solve(int n,long arr[]) {
        sort(arr,0,arr.length-1);
        for(int i=0;i<n-1;i++) {
            if(i%2==0) {
                if(arr[i]!=arr[i+1]) {
                    return false;
                }
            }
        }
        
        Set<Long> hashset=new HashSet<>();
        long sum=0,rem=n;
        for(int i=n-1;i>=0;i-=2,rem-=2) {
            long ele=arr[i]-sum;
            if(ele<=0) {
                return false;
            }
            if(ele%rem!=0) {
                return false;
            }
            long tmp=ele/rem;
            if(tmp==0) {
                return false;
            }
            if(hashset.contains(tmp)) {
                return false;
            }
            hashset.add(tmp);
//            System.out.print(tmp+" ");
            sum+=2*tmp;
        }
//        System.out.println();
        return true;
    }
